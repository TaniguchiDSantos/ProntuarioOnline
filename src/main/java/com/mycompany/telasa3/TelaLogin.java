    private void jLabelSearchPacienteIconActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLabelSearchPacienteIconActionPerformed
        // TODO add your handling code here:
        if (!jTextFieldEmail.getText().equals("")
                && jPasswordFieldSenha.getPassword().length > 0) {

            jPanelEmail.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.BLACK,
                            Color.BLACK, Color.BLACK, Color.BLACK),
                    BorderFactory.createEmptyBorder(
                            jPanelEmail.getBorder().getBorderInsets(jPanelEmail).top,
                            jPanelEmail.getBorder().getBorderInsets(jPanelEmail).left,
                            jPanelEmail.getBorder().getBorderInsets(jPanelEmail).bottom,
                            jPanelEmail.getBorder().getBorderInsets(jPanelEmail).right)));

            jPanelSenha.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.BLACK,
                            Color.BLACK, Color.BLACK, Color.BLACK),
                    BorderFactory.createEmptyBorder(
                            jPanelSenha.getBorder().getBorderInsets(jPanelSenha).top,
                            jPanelSenha.getBorder().getBorderInsets(jPanelSenha).left,
                            jPanelSenha.getBorder().getBorderInsets(jPanelSenha).bottom,
                            jPanelSenha.getBorder().getBorderInsets(jPanelSenha).right)));

            Acesso acesso = new Acesso();
            acesso.setEmail(jTextFieldEmail.getText());
            acesso.setSenha(String.valueOf(jPasswordFieldSenha.getPassword()));
            if (acesso.verificarAcesso() != null) {
                new TelaHome(acesso.verificarAcesso()).setVisible(true); // Open the Second.java window
                dispose(); // Close the First.java window
            } else {
                JOptionPane.showMessageDialog(null, "E mail ou Senha Incorretos",
                        "Erro", JOptionPane.ERROR_MESSAGE);
                jTextFieldEmail.setText("");
                jPasswordFieldSenha.setText("");

            }

        } else {
            if (jTextFieldEmail.getText().equals("")) {
                jPanelEmail.setBorder(BorderFactory.createCompoundBorder(
                        BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.RED,
                                Color.RED, Color.RED, Color.RED),
                        BorderFactory.createEmptyBorder(
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).top,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).left,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).bottom,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).right)));
            } else if (!jTextFieldEmail.getText().equals("")) {
                jPanelEmail.setBorder(BorderFactory.createCompoundBorder(
                        BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.BLACK,
                                Color.BLACK, Color.BLACK, Color.BLACK),
                        BorderFactory.createEmptyBorder(
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).top,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).left,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).bottom,
                                jPanelEmail.getBorder().getBorderInsets(jPanelEmail).right)));
            }

            if (jPasswordFieldSenha.getPassword().length == 0) {
                jPanelSenha.setBorder(BorderFactory.createCompoundBorder(
                        BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.RED,
                                Color.RED, Color.RED, Color.RED),
                        BorderFactory.createEmptyBorder(
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).top,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).left,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).bottom,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).right)));
            } else if (jPasswordFieldSenha.getPassword().length > 0) {
                jPanelSenha.setBorder(BorderFactory.createCompoundBorder(
                        BorderFactory.createSoftBevelBorder(BevelBorder.LOWERED, Color.BLACK,
                                Color.BLACK, Color.BLACK, Color.BLACK),
                        BorderFactory.createEmptyBorder(
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).top,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).left,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).bottom,
                                jPanelSenha.getBorder().getBorderInsets(jPanelSenha).right)));
            }

            JOptionPane.showMessageDialog(null, "Preencha Os Campos Obrigat√≥rios",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelSearchPacienteIconActionPerformed

